    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        try {
            // TODO add your handling code here:
            st = cn.createStatement();
            
            String id_MCU = no_reg.getText();
            String date_MCU = tgl_add.getText();
            String dok_MCU = no_dok.getText();
            String pas_MCU = no_pas.getText();
            String diag = diagnos.getText();
            
            String insert_data = String.format("INSERT INTO mcu Values ('%s','%s','%s','%s','%s')",pas_MCU,dok_MCU,id_MCU,date_MCU,diag);
            st.execute(insert_data);
            JOptionPane.showMessageDialog(null,"success save data");
        } catch (SQLException ex) {
            Logger.getLogger(MCU.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveActionPerformed

