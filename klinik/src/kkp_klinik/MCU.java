    private void baruActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_baruActionPerformed
        try {
            // TODO add your handling code here:
            st = cn.createStatement();
            String auto_id = "SELECT id_mcu from mcu ORDER BY id_mcu DESC LIMIT 1";
            rs = st.executeQuery(auto_id);
            if (rs.next()){
                String old_id = rs.getString("id_mcu").substring(4);
                int auto_inc = Integer.parseInt(old_id);
                auto_inc += 1;
                String s_auto_no = Integer.toString(auto_inc);
                no_reg.setText(String.format("MCU-%s",s_auto_no));
            }
            // add new today date
            LocalDateTime ldt = LocalDateTime.now();
            String latest_date = DateTimeFormatter.ofPattern("yyyy-MM-dd", Locale.ENGLISH).format(ldt);
            tgl_add.setText(latest_date);
//        date_add.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(MCU.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_baruActionPerformed

